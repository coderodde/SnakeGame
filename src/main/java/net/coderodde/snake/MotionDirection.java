package net.coderodde.snake;

/**
 * This enumeration specifies the motion direction of the snake.
 * 
 * @author Rodion "rodde" Efremov
 * @version 1.6 (Dec 8, 2018)
 */
public enum MotionDirection {
    
    /**
     * Moving upwards.
     */
    NORTH,
    
    /**
     * Moving to the right. 
     */
    EAST,
    
    /**
     * Moving downwards.
     */
    SOUTH,
    
    /**
     * Moving to the left.
     */
    WEST,
}
